    @SuppressWarnings("empty-statement")
    private void bookBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookBtnActionPerformed
        // TODO add your handling code here:
        Airliner airliner;
        ManageAirplaneJPanel airPlaneDirectory;
        flight flight;
        seat seat;
        int a = DisplayTable.getSelectedRow();
        if (a != -1){
            airliner = (Airliner) DisplayTable.getValueAt(a, 0);
            airPlaneDirectory = (ManageAirplaneJPanel)DisplayTable.getValueAt(a, 1);
            flight = (flight)DisplayTable.getValueAt(a, 2);;
            seat = (seat)DisplayTable.getValueAt(a,3);
        }
        else{
            JOptionPane.showMessageDialog(null,"Nothing Selected");
            return;
        }
        Booking booking=new Booking(airliner,airPlaneDirectory,flight,seat);
        BookCustomers panel = new BookCustomers(userProcessContainer,booking);
        userProcessContainer.add("BookCustomersJPanel", panel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);

    }//GEN-LAST:event_bookBtnActionPerformed


