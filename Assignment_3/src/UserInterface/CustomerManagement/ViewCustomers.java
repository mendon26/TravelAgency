    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel)DisplayTable.getModel();
        dtm.setRowCount(0);
        for(Airliner al:travelAgency.getAirlinerList()){
            for(Airplane ap: al.getAirplanelist()){
                for(flight fl:ap.getFlightList()){
                    if(fl.getDestination().equals(destinationTxt.getText())&&fl.getSource().equals(sourceTxt.getText())){
                        for(seat st:fl.getSeatList()){
                            if(st.getPrice()<=Integer.valueOf(priceTxt.getText())){
                                Object row[] = new Object[10];
                                row[0]= al;
                                row[1]= ap;
                                row[2]=fl;
                                row[3]=st;
                                row[4]=st.getPrice();
                                dtm.addRow(row);

                            }
                        }
                    }
                }
            }
        }

    }//GEN-LAST:event_searchBtnActionPerformed

